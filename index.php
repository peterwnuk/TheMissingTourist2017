               		walking along the main road heading out of the city and was never seen again.</h4>
               	<h4 class="font-white">THE MISSING TOURIST tells the story of Atsumi Yoshikubo’s mysterious disappearance 
               		and the urgent missing person investigation that followed, with the RCMP ultimately – and controversially 
