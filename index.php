               		events covered in the news to find out what really happened to Atsumi Yoshikubo. With interviews with 
               		family, friends and colleagues in Japan as well as media, police officials and members of the community 
               		in Yellowknife who were among the last to see her, THE MISSING TOURIST attempts to bring a sense of closure 
               		to one of the most unsettling mysteries of the Canadian North in recent memory.

